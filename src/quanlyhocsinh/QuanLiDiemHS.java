package quanlyhocsinh;

public class QuanLiDiemHS {
	private double toan, anh, van, ly, sinh, hoa;

	public QuanLiDiemHS() {
		
	}

	public QuanLiDiemHS(double toan, double anh, double van, double ly, double sinh, double hoa) {
		super();
		this.toan = toan;
		this.anh = anh;
		this.van = van;
		this.ly = ly;
		this.sinh = sinh;
		this.hoa = hoa;
	}

	public double getToan() {
		return toan;
	}

	public void setToan(double toan) {
		this.toan = toan;
	}

	public double getAnh() {
		return anh;
	}

	public void setAnh(double anh) {
		this.anh = anh;
	}

	public double getVan() {
		return van;
	}

	public void setVan(double van) {
		this.van = van;
	}

	public double getLy() {
		return ly;
	}

	public void setLy(double ly) {
		this.ly = ly;
	}

	public double getSinh() {
		return sinh;
	}

	public void setSinh(double sinh) {
		this.sinh = sinh;
	}

	public double getHoa() {
		return hoa;
	}

	public void setHoa(double hoa) {
		this.hoa = hoa;
	}
	
	
	

}
